        protected override void OnUpdateFrame(FrameEventArgs e)
        {
            base.OnUpdateFrame(e);
            float tiempoDelta = (float)e.Time; // Tiempo del frame en segundos
            var keyboardState = Keyboard.GetState();
            // --- ACTUALIZAR ANIMACIÓN ---
            if (animacionDelCoche != null)
            {
                //animacionDelCoche.Actualizar(tiempoDelta); // << NUEVO
            }

            if (keyboardState.IsKeyDown(Key.Number0))  // Mover hacia adelante
            {
                initAnimation = !initAnimation;
            }


            if (keyboardState.IsKeyDown(Key.Number1))  // Mover hacia adelante
            {
                cameraPosition += cameraFront * cameraSpeed;
            }
            if (keyboardState.IsKeyDown(Key.Number2))  // Mover hacia atrás
            {
                cameraPosition -= cameraFront * cameraSpeed;
                
            }
            if (keyboardState.IsKeyDown(Key.Number3))  // Mover a la izquierda
            {
                cameraPosition -= Vector3.Normalize(Vector3.Cross(cameraFront, cameraUp)) * cameraSpeed;
            }
            if (keyboardState.IsKeyDown(Key.Number4))  // Mover a la derecha
            {
                cameraPosition += Vector3.Normalize(Vector3.Cross(cameraFront, cameraUp)) * cameraSpeed;
            }

            if (keyboardState.IsKeyDown(Key.Y))
            {
                //cocheRotationY += rotationSpeed;
                AplicarTransformacionConCentroMasa2(0.5f, new Vector3(0, 1, 0));
            }
            //traslacion

            if (keyboardState.IsKeyDown(Key.U))
            {
                car.Trasladar(new Vector3(1*0.1f, 0, 0));
            }
            if (keyboardState.IsKeyDown(Key.K))
            {
                car.Trasladar(new Vector3(1 * -0.1f, 0, 0));
            }
            if (keyboardState.IsKeyDown(Key.J))
            {
                car.Trasladar(new Vector3(0, 0, 1 * 0.1f));
            }
            if (keyboardState.IsKeyDown(Key.H))
            {
                car.Trasladar(new Vector3(0, 0, 1 * -0.1f));
            }

            if (keyboardState.IsKeyDown(Key.O))
            {
                Parte rueda1 = car.GetParte("ruedaFrontIzq");
                Vector centroMasa = rueda1.CalcularCentroMasa();
                rueda1.Rotar(0.5f, new Vector3(0, 1, 0), centroMasa);
                Parte rueda2 = car.GetParte("ruedaFrontDer");
                centroMasa = rueda2.CalcularCentroMasa();
                rueda2.Rotar(0.5f, new Vector3(0, 1, 0), centroMasa);
            }


            if (keyboardState.IsKeyDown(Key.P))
                Serializador.SerializarObjeto<Escenario>(this.escenarioU,"escenario.json");

        }



